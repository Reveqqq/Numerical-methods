from docx import Document


# Create a Word document

doc = Document()


# Title

doc.add_heading('Отчет по итерационным методам решения СЛАУ', level=1)


# Introduction

doc.add_heading('Введение', level=2)

doc.add_paragraph(

    "В данной работе выполнена сравнительная оценка эффективности нескольких итерационных методов решения системы "

    "линейных уравнений Ax = b, где A — квадратная матрица, а b — вектор. Рассмотрены следующие методы:"

)

doc.add_paragraph("1. Метод Якоби")

doc.add_paragraph("2. Метод Зейделя")

doc.add_paragraph("3. Метод релаксации (SOR)")

doc.add_paragraph("4. Метод простой итерации")

doc.add_paragraph(

    "Для каждого метода исследована зависимость количества итераций от параметров сходимости (ϵ) и специфических "

    "параметров метода (ω для релаксации и τ для простой итерации)."

)


# Method Jacobi

doc.add_heading('Метод Якоби', level=2)

doc.add_paragraph("Особенности:")

doc.add_paragraph(

    "- Каждое значение вектора x вычисляется независимо, основываясь на данных предыдущей итерации."

)

doc.add_paragraph("Результаты:")

doc.add_paragraph(

    "- Увеличение количества итераций при уменьшении ϵ, что соответствует повышению точности решения."

)

doc.add_paragraph(

    "- Логарифмическая зависимость количества итераций от ϵ, указывающая на медленную сходимость."

)

doc.add_paragraph(

    "- Метод менее эффективен, особенно при строгих требованиях к точности (ϵ → 0)."

)


# Method Seidel

doc.add_heading('Метод Зейделя', level=2)

doc.add_paragraph("Особенности:")

doc.add_paragraph(

    "- Значения вектора x обновляются последовательно в процессе вычислений, что ускоряет сходимость."

)

doc.add_paragraph("Результаты:")

doc.add_paragraph(

    "- Более быстрая сходимость по сравнению с методом Якоби."

)

doc.add_paragraph(

    "- Общая зависимость количества итераций от ϵ схожа с методом Якоби, но требуемое количество итераций существенно меньше."

)

doc.add_paragraph(

    "- Метод хорошо подходит для систем с хорошей обусловленностью."

)


# Method Relaxation

doc.add_heading('Метод релаксации (SOR)', level=2)

doc.add_paragraph("Особенности:")

doc.add_paragraph(

    "- Введение параметра ω для управления скоростью сходимости:"

)

doc.add_paragraph(

    "  - При ω = 1.0 метод сводится к Зейделю."

)

doc.add_paragraph(

    "  - При ω > 1.0 возможно ускорение сходимости, если параметр выбран оптимально."

)

doc.add_paragraph("Результаты:")

doc.add_paragraph(

    "- Минимальное количество итераций достигается при ω ≈ 1.0-1.5, подтверждая существование оптимального значения."

)

doc.add_paragraph(

    "- Зависимость количества итераций от ϵ аналогична другим методам."

)

doc.add_paragraph(

    "- При некорректном выборе ω (особенно около 0 или 2) наблюдается ухудшение сходимости."

)


# Method Iteration

doc.add_heading('Метод простой итерации', level=2)

doc.add_paragraph("Особенности:")

doc.add_paragraph(

    "- Использование фиксированного параметра τ, определяющего скорость сходимости."

)

doc.add_paragraph("Результаты:")

doc.add_paragraph(

    "- Выбор τ значительно влияет на скорость сходимости."

)

doc.add_paragraph(

    "- Для каждого значения ϵ возможно подобрать оптимальное τ, при котором количество итераций минимально."

)

doc.add_paragraph(

    "- Зависимость количества итераций от ϵ сохраняет тенденции, аналогичные другим методам."

)


# Summary

doc.add_heading('Итоговое сравнение', level=2)

doc.add_paragraph(
    "Метод Якоби оказался наиболее медленным и чувствительным к уменьшению ϵ.")

doc.add_paragraph(
    "Метод Зейделя демонстрирует существенное преимущество в скорости сходимости, оставаясь простым в реализации.")

doc.add_paragraph(
    "Метод релаксации при оптимальном выборе ω оказался самым быстрым, но требует точной настройки параметра.")

doc.add_paragraph(
    "Метод простой итерации эффективен при правильном выборе параметра τ, но менее универсален.")


# Recommendations

doc.add_heading('Рекомендации', level=2)

doc.add_paragraph(
    "1. Метод Якоби: Использовать для задач, требующих параллелизации вычислений.")

doc.add_paragraph(
    "2. Метод Зейделя: Рекомендуется для задач с хорошо обусловленными системами.")

doc.add_paragraph(
    "3. Метод релаксации: Наиболее эффективен при корректной настройке ω, рекомендуется для задач с высокой сложностью.")

doc.add_paragraph(
    "4. Метод простой итерации: Применим в задачах с априорной информацией о параметре τ.")


# Save the document

file_path = "C:\Учеба\Выч мат 3 курс\Отчет_итерационные_методы.docx"

doc.save(file_path)
