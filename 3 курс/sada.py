
from docx import Document

# Create a Word document
doc = Document()

# Title
doc.add_heading("Краткий отчет по проведенному исследованию", level=1)

# Objective
doc.add_heading("Цель работы:", level=2)
doc.add_paragraph(
    "Решение системы линейных уравнений методом простой итерации с изучением зависимости количества итераций от параметров τ и ε."
)

# Methodology
doc.add_heading("Методология:", level=2)
doc.add_paragraph(
    "1. Математическая постановка задачи:\n"
    "   - Представлена система x = Bx + c, где B = E - τA, c = τb.\n"
    "   - Метод итераций использует начальное приближение x₀ = 0 и выполняется до достижения условия ||x_new - x_old|| < ε."
)
doc.add_paragraph(
    "2. Параметры исследования:\n"
    "   - τ — коэффициент релаксации, исследован диапазон [0.1, 1.0] с шагом 0.1.\n"
    "   - ε — точность решения, использованы значения 10⁻², 10⁻³, 10⁻⁴, 10⁻⁵."
)
doc.add_paragraph(
    "3. Критерий оценки:\n"
    "   - Количество итераций до достижения заданной точности (ε)."
)
doc.add_paragraph(
    "4. Анализ:\n"
    "   - Количество итераций исследовано отдельно для каждого τ и ε.\n"
    "   - Группировка данных выполнена для выявления зависимостей:\n"
    "     - По τ — среднее количество итераций для фиксированного τ.\n"
    "     - По ε — среднее количество итераций для фиксированного ε."
)

# Results
doc.add_heading("Результаты:", level=2)
doc.add_paragraph(
    "1. Зависимость количества итераций от τ:\n"
    "   - Увеличение τ уменьшает количество итераций для большинства значений ε.\n"
    "   - При больших значениях τ метод становится менее устойчивым."
)
doc.add_paragraph(
    "2. Зависимость количества итераций от ε:\n"
    "   - Более строгие значения ε (10⁻⁵) требуют большего количества итераций для сходимости.\n"
    "   - При фиксированном τ снижение ε увеличивает время расчета."
)
doc.add_paragraph(
    "3. Решения:\n"
    "   - Для каждого случая рассчитаны решения x, которые хранятся в соответствующей таблице."
)

# Conclusions
doc.add_heading("Выводы:", level=2)
doc.add_paragraph(
    "1. Метод итерации чувствителен к выбору параметра τ. Оптимальный диапазон для τ зависит от спектра собственных значений матрицы A.\n"
    "2. Значение точности ε прямо пропорционально количеству итераций: при увеличении точности расчет становится более трудоемким.\n"
    "3. Включение аналитики по τ и ε позволяет выбрать наиболее подходящие параметры для итерационного метода."
)

# Graphs
doc.add_heading("Графики:", level=2)
doc.add_paragraph(
    "Были построены графики зависимости количества итераций от:\n"
    "- τ для фиксированных ε.\n"
    "- ε для фиксированных τ.\n"
    "Эти графики наглядно показывают характер изменения скорости сходимости метода."
)

# Save the document
doc.save()
